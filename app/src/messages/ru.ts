/** Подпись канала контакта для UI (в БД хранится EMAIL и т.д. — не показываем капсом). */
export function formatContactChannelType(code: string): string {
  const key = code.trim().toUpperCase();
  const map = contactChannelLabels as Record<string, string | undefined>;
  return map[key] ?? code;
}

